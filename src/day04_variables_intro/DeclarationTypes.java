package day04_variables_intro;

public class DeclarationTypes {
    public static void main(String[] args){
        //declaration + assignment in single statement:
        int bananas = 34;
        System.out.println(bananas);

        int students = 400;
        System.out.println(students);

        int phonebook=100, textbook=200, computers=300;
        System.out.println(phonebook+textbook+computers);

        int peopleInCity=1000, birdsOnTree=2000, fishInLake=3000;
        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);

        int studentsInNY = 25, studentsInVA = 50, studentsInCO = 5;
        System.out.println(studentsInNY);
        System.out.println(studentsInVA);
        System.out.println(studentsInCO);



    }
}
